function zeroChangeColor() {
            if ($("#zero").hasClass("red")) {
                $("#zero").removeClass("red");
                $("#zero").addClass("yellow");
            }
            else if ($("#zero").hasClass("yellow")) {
                $("#zero").removeClass("yellow");
                $("#zero").addClass("blue");
            }
            else if ($("#zero").hasClass("blue")) {
                $("#zero").removeClass("blue");
                $("#zero").addClass("red");
            }
}

